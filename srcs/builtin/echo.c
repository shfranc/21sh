#include "shell21.h"

void	ft_builtin_echo(char **cmd)
{
	int	i;
	int n;

	n = 0;
	i = 1;
	if (ft_strequ(*(cmd + 1), "-n"))
	{
		n = 1;
		i++;
	}
	while (*(cmd + i))
	{
		ft_putstr(*(cmd + i));
		if (*(cmd + ++i))
			write(1, " ", 1);
	}
	if (n == 0)
		write(1, "\n", 1);
}
